% --- Executes on button press in pushbutton1.
function pushbutton1_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton1 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
data = readmatrix('toko_sepatu.xlsx','Range','B:F');
set(handles.table,'data',data);
k = [1,0,1,0,0];
w = [0.30,0.25,0.15,0.20,0.10];
[m, n] = size(data);
R = zeros(m,n);
Y = zeros(m,n);
for j=1:n
    if k(j)==1
        R(:,j)=data(:,j)./max(data(:,j));
    else
        R(:,j)=min(data(:,j))./data(:,j);
    end
end
for i=1:m
 V(i)= sum(w.*R(i,:));
end
set(handles.edit2,'string',num2str(V(1)));
set(handles.edit3,'string',num2str(V(2)));
set(handles.edit4,'string',num2str(V(3)));
set(handles.edit5,'string',num2str(V(4)));

